class Solution {
public:
    bool isOneBitCharacter(vector<int>& bits) {
        int i,n;
        i=0,n=bits.size();
        while(i<n){
            if(bits[i]==1){
                i+=2;
                if(i==n)
                return 0;
            }
            else
            i++;
        }
        return 1;
    }
};
